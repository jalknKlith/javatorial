package codes;

public class doWhile {
    public static void main(String[] args) {
        // do while loop
        int i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i < 5);
        System.out.println("--------------");
        // while loop
        int z = 0;
        while (z < 5) {
            System.out.println(z);
            z++;
        }
    }
}