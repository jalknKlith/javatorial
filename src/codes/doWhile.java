package codes;

public class doWhile {
    public static void main(String[] args) {
        // do while loop
        int i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i < 5);
        System.out.println("--------------");
        // while loop
        int x = 0;
        while (x < 5) {
            System.out.println(x);
            x++;
        }
    }
}